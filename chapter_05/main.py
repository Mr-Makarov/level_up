# Упр 5.1-2. Проверка условий
# 1. Проверка равенства строк (True)
car = 'subaru'
print("Is car == 'subaru'? I predict True.")
print(car == 'subaru')

# 2. Проверка неравенства строк (False)
print("\nIs car != 'subaru'? I predict False.")
print(car != 'subaru')

# 3. Проверка с числовым равенством (True)
age = 25
print("\nIs age == 25? I predict True.")
print(age == 25)

# 4. Проверка с числовым неравенством (False)
print("\nIs age == 30? I predict False.")
print(age == 30)

# 5. Проверка больше/меньше (True)
temperature = 18
print("\nIs temperature < 20? I predict True.")
print(temperature < 20)

# 6. Проверка больше или равно (False)
print("\nIs temperature >= 25? I predict False.")
print(temperature >= 25)

# 7. Проверка with and (True)
score = 85
print("\nIs score > 50 and score < 100? I predict True.")
print(score > 50 and score < 100)

# 8. Проверка with or (False)
print("\nIs score < 60 or score > 90? I predict False.")
print(score < 60 or score > 90)

# 9. Проверка наличия элемента в списке (True)
colors = ['red', 'green', 'blue']
print("\nIs 'green' in colors? I predict True.")
print('green' in colors)

# 10. Проверка отсутствия элемента в списке (False)
print("\nIs 'yellow' not in colors? I predict False.")
print('yellow' not in colors)

